// Confirms the wire format actually carries the field numbers we cited in
// FLEET_SECURITY.md §3.1: AdminMessage.set_channel = 33, MeshPacket.pki_encrypted = 17.
// Decodes the raw bytes (not the high-level helpers) to verify the field tags
// generated by protoc match the table; if the upstream proto ever renumbers
// these, this test fails before any integration test does.
func TestBuildAdminPacket_FieldNumbersMatchPlan(t *testing.T) {
	frame, _, err := BuildAdminPacketPKC(0xa1b2c3d4, makeSetChannelAdmin(0, "x", nil))
	if err != nil {
		t.Fatalf("BuildAdminPacketPKC: %v", err)
	}
	var tr pb.ToRadio
	if err := proto.Unmarshal(frame, &tr); err != nil {
		t.Fatalf("unmarshal: %v", err)
	}
	pkt := tr.GetPacket()
	// Use protoreflect to inspect actual field numbers used by this codepath.
	pktDesc := pkt.ProtoReflect().Descriptor()
	pkiField := pktDesc.Fields().ByName("pki_encrypted")
	if pkiField == nil {
		t.Fatal("MeshPacket has no pki_encrypted field")
	}
	if got := pkiField.Number(); got != 17 {
		t.Errorf("pki_encrypted field = %d, want 17 (per FLEET_SECURITY.md §3.1)", got)
	}

	adminDesc := (&pb.AdminMessage{}).ProtoReflect().Descriptor()
	setCh := adminDesc.Fields().ByName("set_channel")
	if setCh == nil {
		t.Fatal("AdminMessage has no set_channel field")
	}
	if got := setCh.Number(); got != 33 {
		t.Errorf("set_channel field = %d, want 33 (per FLEET_SECURITY.md §3.1)", got)
	}

	// Also check SecurityConfig.admin_key (field 3, repeated bytes).
	secDesc := (&pb.Config_SecurityConfig{}).ProtoReflect().Descriptor()
	adminKey := secDesc.Fields().ByName("admin_key")
	if adminKey == nil {
		t.Fatal("SecurityConfig has no admin_key field")
	}
	if got := adminKey.Number(); got != 3 {
		t.Errorf("admin_key field = %d, want 3", got)
	}
	if !adminKey.IsList() {
		t.Error("admin_key should be repeated")
	}
}
